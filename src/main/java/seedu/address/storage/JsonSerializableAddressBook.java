package seedu.address.storage;

import java.util.ArrayList;
import java.util.List;
import java.util.stream.Collectors;

import com.fasterxml.jackson.annotation.JsonCreator;
import com.fasterxml.jackson.annotation.JsonProperty;
import com.fasterxml.jackson.annotation.JsonRootName;

import seedu.address.commons.exceptions.IllegalValueException;
import seedu.address.model.AddressBook;
import seedu.address.model.ReadOnlyAddressBook;
import seedu.address.model.recruit.Recruit;

/**
 * An Immutable AddressBook that is serializable to JSON format.
 */
@JsonRootName(value = "addressbook")
class JsonSerializableAddressBook {

    public static final String MESSAGE_DUPLICATE_RECRUIT = "Recruits list contains duplicate recruit(s).";

    private final List<JsonAdaptedRecruit> recruits = new ArrayList<>();

    /**
     * Constructs a {@code JsonSerializableAddressBook} with the given persons.
     */
    @JsonCreator
    public JsonSerializableAddressBook(@JsonProperty("recruits") List<JsonAdaptedRecruit> recruits) {
        this.recruits.addAll(recruits);
    }

    /**
     * Converts a given {@code ReadOnlyAddressBook} into this class for Jackson use.
     *
     * @param source future changes to this will not affect the created {@code JsonSerializableAddressBook}.
     */
    public JsonSerializableAddressBook(ReadOnlyAddressBook source) {
        recruits.addAll(source.getRecruitList().stream().map(JsonAdaptedRecruit::new).collect(Collectors.toList()));
    }

    /**
     * Converts this address book into the model's {@code AddressBook} object.
     *
     * @throws IllegalValueException if there were any data constraints violated.
     */
    public AddressBook toModelType() throws IllegalValueException {
        AddressBook addressBook = new AddressBook();
        for (JsonAdaptedRecruit jsonAdaptedRecruit : recruits) {
            Recruit recruit = jsonAdaptedRecruit.toModelType();
            if (addressBook.hasRecruit(recruit)) {
                throw new IllegalValueException(MESSAGE_DUPLICATE_RECRUIT);
            }
            addressBook.addRecruit(recruit);
        }
        return addressBook;
    }

}
